const { Client, GatewayIntentBits } = require("discord.js");
const config = require("./config.js");
const fs = require("fs");
const path = require('path');
const { printWatermark } = require('./util/pw');
const { initializePlayer } = require('./player');

const client = new Client({
    intents: Object.keys(GatewayIntentBits).map((a) => {
        return GatewayIntentBits[a];
    }),
});

client.config = config;
initializePlayer(client);

client.on("ready", () => {
  console.log(`Logged in as ${client.user.tag}`);
  client.riffy.init(client.user.id);
});

client.config = config;

fs.readdir("./events", (_err, files) => {
  files.forEach((file) => {
    if (!file.endsWith(".js")) return;
    const event = require(`./events/${file}`);
    let eventName = file.split(".")[0]; 
    client.on(eventName, event.bind(null, client));
    delete require.cache[require.resolve(`./events/${file}`)];
  });
});


client.commands = [];
fs.readdir(config.commandsDir, (err, files) => {
  if (err) throw err;
  files.forEach(async (f) => {
    try {
      if (f.endsWith(".js")) {
        let props = require(`${config.commandsDir}/${f}`);
        client.commands.push({
          name: props.name,
          description: props.description,
          options: props.options,
        });
      }
    } catch (err) {
      console.log(err);
    }
  });
});


client.on("raw", (d) => {
    const { GatewayDispatchEvents } = require("discord.js");
    if (![GatewayDispatchEvents.VoiceStateUpdate, GatewayDispatchEvents.VoiceServerUpdate].includes(d.t)) return;
    client.riffy.updateVoiceState(d);
});

client.login(config.TOKEN || process.env.TOKEN).catch((e) => {
    console.log('TOKEN ERROR❌  - Turn On Intents or Reset New Token');
});

const express = require("express");
const app = express();
const port = 3000;
app.get('/', (req, res) => {
    const imagePath = path.join(__dirname, 'index.html');
    res.sendFile(imagePath);
});
app.listen(port, () => {
    console.log(`🔗 Listening to GlaceYT : http://localhost:${port}`);
});

printWatermark();










const channelId = '1073430134268383252'; // 通知を送信するチャンネルIDを置き換える

client.on('voiceStateUpdate', async (oldState, newState) => {
    const member = newState.member;
    const channel = client.channels.cache.get(channelId);

    if (!member || member.bot || newState.channel === null) return;

    if (newState.channelId === channelId) {
        const embed = new Discord.MessageEmbed()
            .setTitle('ボイスチャンネルに参加しました')
            .setDescription(`${member.user.tag}さんが ${newState.channel.name} に参加しました。`)
            .setTimestamp()
            .setColor('#0099ff');
        channel.send(embed);
    } else if (oldState.channelId === channelId) {
        const embed = new Discord.MessageEmbed()
            .setTitle('ボイスチャンネルを退出しました')
            .setDescription(`${member.user.tag}さんが ${oldState.channel.name} から退出しました。`)
            .setTimestamp()
            .setColor('#ff0000');
        channel.send(embed);
    }
});





